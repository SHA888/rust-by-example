fn main() {
    let number = 3;

    if number < 5 {
        println!("condition was true");
    } else {
        println!("condition was false");
    }

    let number2 = 7;

    if number2 < 5 {
        println!("condition was true");
    } else {
        println!("condition was false");
    }
    
    let number3 = 3;

    if number3 != 0 {
        println!("condition was something other than zero");
    }

    // Handling Multiple Conditions with else if
}
